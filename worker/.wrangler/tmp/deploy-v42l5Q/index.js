async function jobDispatchCron(env) {
  const now = Date.now();
  const dueJobs = await env.DB.prepare(
    "SELECT * FROM cron_jobs WHERE status = 'active' AND next_run_at <= ? ORDER BY next_run_at ASC LIMIT 50"
  ).bind(now).all();
  for (const job of dueJobs.results) {
    try {
      await dispatchJob(job, env);
      const nextRun = calculateNextRun(job.schedule, now);
      await env.DB.prepare("UPDATE cron_jobs SET last_run_at = ?, next_run_at = ? WHERE id = ?").bind(now, nextRun, job.id).run();
      await logAudit(
        {
          userId: job.user_id,
          serverId: job.server_id,
          action: "cron.job.dispatched",
          details: { jobId: job.id, jobName: job.name, action: job.action }
        },
        env.DB
      );
    } catch (err) {
      logger_default.error({ jobId: job.id, err }, "Cron job dispatch failed");
    }
  }
  if (dueJobs.results.length > 0) {
    logger_default.info({ dispatched: dueJobs.results.length }, "Job dispatch cron complete");
  }
}
