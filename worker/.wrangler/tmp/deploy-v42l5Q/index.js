var PING = 1;
var DiscordHandler = class {
  static {
    __name(this, "DiscordHandler");
  }
  getCapabilities() {
    return {
      maxMessageLength: 2e3,
      supportsThreadedReplies: true,
      supportsMarkdown: true,
      supportsCodeBlocks: true,
      rateLimitPerMin: 30,
      requiredConfigKeys: ["botToken", "publicKey"]
    };
  }
  async verifyInbound(req, config) {
    const signature = req.headers.get("X-Signature-Ed25519");
    const timestamp = req.headers.get("X-Signature-Timestamp");
    if (!signature || !timestamp) return false;
    const tsSeconds = parseInt(timestamp, 10);
    if (isNaN(tsSeconds) || Math.abs(Date.now() / 1e3 - tsSeconds) > 300) return false;
    const publicKey = config.config.publicKey;
    if (!publicKey) return false;
    const body = await req.clone().text();
    const message = timestamp + body;
    try {
      const publicKeyBytes = hexToBytes(publicKey);
      const signatureBytes = hexToBytes(signature);
      const messageBytes = new TextEncoder().encode(message);
      const cryptoKey = await crypto.subtle.importKey(
        "raw",
        publicKeyBytes,
        { name: "Ed25519" },
        false,
        ["verify"]
      );
      return await crypto.subtle.verify("Ed25519", cryptoKey, signatureBytes, messageBytes);
    } catch {
      return false;
    }
  }
  async normalizeInbound(req, config) {
    const body = await req.json();
    if (body.type === PING) {
      return {
        platform: "discord",
        botId: config.botId,
        channelId: "ping",
        userId: "discord",
        content: "__ping__",
        isCommand: false,
        raw: body
      };
    }
    const channelId = body.channel_id ?? body.channel?.id ?? "";
    const userId = body.member?.user?.id ?? body.user?.id ?? "";
    const guildId = body.guild_id;
    if (body.type === 2) {
      const cmdName = body.data?.name ?? "";
      const options = body.data?.options ?? [];
      const args = options.map((o) => String(o.value));
      const content2 = args.join(" ");
      return {
        platform: "discord",
        botId: config.botId,
        channelId: guildId ? `${guildId}:${channelId}` : channelId,
        userId,
        content: content2,
        messageId: body.id,
        isCommand: true,
        command: cmdName,
        args,
        raw: body
      };
    }
    const content = body.data?.custom_id ?? body.message?.content ?? "";
    return {
      platform: "discord",
      botId: config.botId,
      channelId: guildId ? `${guildId}:${channelId}` : channelId,
      userId,
      content,
      messageId: body.id,
      isCommand: false,
      raw: body
    };
  }
  async sendOutbound(msg, config) {
    const channelId = msg.channelId.includes(":") ? msg.channelId.split(":")[1] : msg.channelId;
    const caps = this.getCapabilities();
    const chunks = splitMessage(msg.content, caps.maxMessageLength);
    for (const chunk of chunks) {
      const payload = { content: chunk };
      if (msg.replyToId) {
        payload.message_reference = { message_id: msg.replyToId };
      }
      const res = await fetch(`https://discord.com/api/v10/channels/${channelId}/messages`, {
        method: "POST",
        headers: {
          "Authorization": `Bot ${config.config.botToken}`,
          "Content-Type": "application/json"
        },
        body: JSON.stringify(payload)
      });
      if (!res.ok) {
        const err = await res.text();
        throw new Error(`Discord API error ${res.status}: ${err}`);
      }
    }
  }
};
function hexToBytes(hex) {
  const len = hex.length / 2;
  const bytes = new Uint8Array(len);
  for (let i = 0; i < len; i++) {
    bytes[i] = parseInt(hex.slice(i * 2, i * 2 + 2), 16);
  }
  return bytes;
}
