var webhookRoutes = new Hono2();
webhookRoutes.post("/:platform/:botId", async (c) => {
  const platform = c.req.param("platform");
  const botId = c.req.param("botId");
  const handler = getHandler(platform);
  if (!handler) {
    return c.json({ error: "unknown_platform" }, 404);
  }
  if (!c.env.BOT_ENCRYPTION_KEY) {
    logger_default.error({}, "BOT_ENCRYPTION_KEY is not configured");
    return c.json({ error: "server_misconfigured" }, 500);
  }
  const row = await c.env.DB.prepare(
    "SELECT id, user_id, platform, config_encrypted FROM platform_bots WHERE id = ? AND platform = ?"
  ).bind(botId, platform).first();
  if (!row) {
    return c.json({ error: "bot_not_found" }, 404);
  }
  let decryptedConfig;
  try {
    decryptedConfig = await decryptBotConfig(row.config_encrypted, c.env.BOT_ENCRYPTION_KEY);
  } catch (err) {
    logger_default.error({ botId, err }, "Failed to decrypt bot config");
    return c.json({ error: "server_error" }, 500);
  }
  const botConfig = {
    botId: row.id,
    userId: row.user_id,
    platform: row.platform,
    config: decryptedConfig
  };
  let verified;
  try {
    verified = await handler.verifyInbound(c.req.raw, botConfig);
  } catch (err) {
    logger_default.error({ platform, botId, err }, "verifyInbound threw");
    return c.json({ error: "verification_error" }, 500);
  }
  if (!verified) {
    logger_default.warn({ platform, botId }, "Webhook signature verification failed");
    return c.json({ error: "unauthorized" }, 401);
  }
  let msg;
  try {
    msg = await handler.normalizeInbound(c.req.raw, botConfig);
  } catch (err) {
    logger_default.error({ platform, botId, err }, "normalizeInbound threw");
    return c.json({ error: "normalization_error" }, 400);
  }
  if (platform === "discord" && msg.channelId === "ping" && msg.content === "__ping__") {
    return c.json({ type: 1 });
  }
  if (platform === "feishu" && msg.channelId === "challenge") {
    const body = await c.req.raw.clone().json().catch(() => ({ challenge: "" }));
    return c.json({ challenge: body.challenge });
  }
  const rateLimitId = c.env.RATE_LIMITER.idFromName(`msg:${platform}:${msg.userId}`);
  const rateLimitStub = c.env.RATE_LIMITER.get(rateLimitId);
  const rlRes = await rateLimitStub.fetch(
    new Request(`https://dummy/?key=${encodeURIComponent(`${platform}:${msg.userId}`)}&type=msg`)
  );
  const { allowed, retryAfter } = await rlRes.json();
  if (!allowed) {
    return c.json({ error: "rate_limited", retryAfter }, 429);
  }
  try {
    await routeInbound(msg, c.env, botId);
  } catch (err) {
    logger_default.error({ platform, botId, channelId: msg.channelId, err }, "routeInbound failed");
    return c.json({ error: "routing_error" }, 500);
  }
  return c.json({ ok: true });
});

// src/security/authorization.ts
async function resolveAuth(c) {
  const authHeader = c.req.header("Authorization");
  if (!authHeader?.startsWith("Bearer ")) return null;
  const token = authHeader.slice(7);
  if (token.startsWith("deck_")) {
    const keyHash = await sha256Hex(token);
    const now = Date.now();
    const row = await c.env.DB.prepare(
      `SELECT user_id FROM api_keys
       WHERE key_hash = ?
         AND revoked_at IS NULL
         AND (grace_expires_at IS NULL OR grace_expires_at > ?)`
    ).bind(keyHash, now).first();
    if (!row) return null;
    return { userId: row.user_id, role: "member" };
  }
  if (!c.env.JWT_SIGNING_KEY) return null;
  const payload = await verifyJwt(token, c.env.JWT_SIGNING_KEY);
  if (!payload) return null;
  if (typeof payload.sub !== "string") return null;
  return { userId: payload.sub, role: payload.role ?? "member" };
}
__name(resolveAuth, "resolveAuth");
function requireAuth() {
  return async (c, next) => {
    const auth = await resolveAuth(c);
    if (!auth) return c.json({ error: "unauthorized" }, 401);
    c.set("userId", auth.userId);
    c.set("role", auth.role);
    await next();
  };
}
__name(requireAuth, "requireAuth");
async function checkServerTeamAccess(c, serverId, userId) {
  const server = await c.env.DB.prepare("SELECT team_id, user_id FROM servers WHERE id = ?").bind(serverId).first();
  if (!server) return false;
  if (server.user_id === userId) return true;
  if (server.team_id) {
    const member = await c.env.DB.prepare("SELECT role FROM team_members WHERE team_id = ? AND user_id = ?").bind(server.team_id, userId).first();
    return !!member;
  }
  return false;
}
