var AUTH_TIMEOUT_MS = 5e3;
var MAX_QUEUE_SIZE = 100;
var MAX_MSG_BYTES = 65536;
var IDLE_TIMEOUT_MS = 12e4;
var DaemonBridge = class {
  static {
    __name(this, "DaemonBridge");
  }
  env;
  ws = null;
  authenticated = false;
  queue = [];
  authTimer;
  idleTimer;
  browserSockets = /* @__PURE__ */ new Set();
  constructor(_state, env) {
    this.env = env;
  }
  async fetch(req) {
    const url = new URL(req.url);
    if (url.pathname === "/daemon") {
      if (req.headers.get("Upgrade") !== "websocket") {
        return new Response("Expected WebSocket", { status: 426 });
      }
      const { 0: client, 1: server } = new WebSocketPair();
      server.accept();
      this.handleDaemonSocket(server);
      return new Response(null, { status: 101, webSocket: client });
    }
    if (url.pathname === "/terminal") {
      if (req.headers.get("Upgrade") !== "websocket") {
        return new Response("Expected WebSocket", { status: 426 });
      }
      const { 0: client, 1: server } = new WebSocketPair();
      server.accept();
      this.handleBrowserSocket(server);
      const sessionName = url.searchParams.get("session");
      if (sessionName && this.ws && this.authenticated) {
        this.ws.send(JSON.stringify({ type: "terminal.subscribe", session: sessionName }));
      }
      return new Response(null, { status: 101, webSocket: client });
    }
    if (url.pathname === "/browser") {
      if (req.headers.get("Upgrade") !== "websocket") {
        return new Response("Expected WebSocket", { status: 426 });
      }
      const { 0: client, 1: server } = new WebSocketPair();
      server.accept();
      this.handleBrowserSocket(server);
      return new Response(null, { status: 101, webSocket: client });
    }
    if (url.pathname === "/send" && req.method === "POST") {
      const msg = await req.json();
      this.sendToDaemon(JSON.stringify(msg));
      return new Response("ok");
    }
    return new Response("Not Found", { status: 404 });
  }
  handleDaemonSocket(ws) {
    this.ws = ws;
    this.authTimer = setTimeout(() => {
      if (!this.authenticated) {
        ws.close(4001, "Auth timeout");
      }
    }, AUTH_TIMEOUT_MS);
    ws.addEventListener("message", async (event) => {
      const data = typeof event.data === "string" ? event.data : "";
      if (data.length > MAX_MSG_BYTES) {
        ws.close(4009, "Message too large");
        return;
      }
      let msg;
      try {
        msg = JSON.parse(data);
      } catch {
        return;
      }
      if (!this.authenticated) {
        if (msg.type === "auth" && typeof msg.serverId === "string" && typeof msg.token === "string") {
          const server = await this.env.DB.prepare("SELECT token_hash FROM servers WHERE id = ?").bind(msg.serverId).first();
          if (server) {
            const hash = await sha256Hex(msg.token);
            if (hash === server.token_hash) {
              this.authenticated = true;
              if (this.authTimer !== void 0) clearTimeout(this.authTimer);
              ws.send(JSON.stringify({ type: "auth_ok" }));
              this.resetIdleTimer(ws);
              for (const q of this.queue) ws.send(q.data);
              this.queue = [];
              return;
            }
          }
        }
        ws.close(4001, "Auth failed");
        return;
      }
      this.resetIdleTimer(ws);
      if (msg.type === "terminal_update" || msg.type === "outbound") {
        for (const bs of this.browserSockets) {
          try {
            bs.send(data);
          } catch {
            this.browserSockets.delete(bs);
          }
        }
      }
    });
    ws.addEventListener("close", () => {
      this.ws = null;
      this.authenticated = false;
      if (this.idleTimer) clearTimeout(this.idleTimer);
    });
  }
  resetIdleTimer(ws) {
    if (this.idleTimer) clearTimeout(this.idleTimer);
    this.idleTimer = setTimeout(() => {
      ws.close(4e3, "Idle timeout");
    }, IDLE_TIMEOUT_MS);
  }
  handleBrowserSocket(ws) {
    this.browserSockets.add(ws);
    ws.addEventListener("close", () => this.browserSockets.delete(ws));
    ws.addEventListener("message", (event) => {
      if (typeof event.data === "string") this.sendToDaemon(event.data);
    });
  }
  sendToDaemon(data) {
    if (this.ws && this.authenticated) {
      this.ws.send(data);
    } else {
      if (this.queue.length < MAX_QUEUE_SIZE) {
        this.queue.push({ data, timestamp: Date.now() });
      }
    }
  }
};

// durable-objects/RateLimiter.ts
var MSG_LIMIT = 30;
var MSG_WINDOW_MS = 6e4;
var BIND_LIMIT = 3;
var BIND_WINDOW_MS = 36e5;
var AUTH_LOCKOUT_ATTEMPTS = 5;
var AUTH_LOCKOUT_MS = 15 * 6e4;
var RateLimiter = class {
  static {
    __name(this, "RateLimiter");
  }
  state;
  constructor(state, _env) {
    this.state = state;
  }
  async fetch(req) {
    const url = new URL(req.url);
    const key = url.searchParams.get("key") ?? "default";
    const type = url.searchParams.get("type") ?? "msg";
    if (type === "msg") {
      const allowed = await this.checkWindow(key, MSG_LIMIT, MSG_WINDOW_MS);
      return Response.json({ allowed, retryAfter: allowed ? 0 : Math.ceil(MSG_WINDOW_MS / 1e3) });
    }
    if (type === "bind") {
      const allowed = await this.checkWindow(key, BIND_LIMIT, BIND_WINDOW_MS);
      return Response.json({ allowed, retryAfter: allowed ? 0 : Math.ceil(BIND_WINDOW_MS / 1e3) });
    }
    if (type === "auth_fail") {
      const result = await this.recordAuthFailure(key);
      return Response.json(result);
    }
    if (type === "auth_check") {
      const result = await this.checkAuthLockout(key);
      return Response.json(result);
    }
    return new Response("Unknown type", { status: 400 });
  }
  async checkWindow(key, limit, windowMs) {
    const data = await this.state.storage.get(key) ?? { timestamps: [] };
    const now = Date.now();
    const windowStart = now - windowMs;
    data.timestamps = data.timestamps.filter((t) => t > windowStart);
    if (data.timestamps.length >= limit) {
      await this.state.storage.put(key, data);
      return false;
    }
    data.timestamps.push(now);
    await this.state.storage.put(key, data);
    return true;
  }
  async recordAuthFailure(key) {
    const storeKey = `auth:${key}`;
    const data = await this.state.storage.get(storeKey) ?? { attempts: 0 };
    if (data.lockedUntil && data.lockedUntil > Date.now()) {
      return { locked: true, lockedUntil: data.lockedUntil };
    }
    data.attempts++;
    if (data.attempts >= AUTH_LOCKOUT_ATTEMPTS) {
      data.lockedUntil = Date.now() + AUTH_LOCKOUT_MS;
      data.attempts = 0;
    }
    await this.state.storage.put(storeKey, data);
    return { locked: !!data.lockedUntil && data.lockedUntil > Date.now(), lockedUntil: data.lockedUntil };
  }
  async checkAuthLockout(key) {
    const storeKey = `auth:${key}`;
    const data = await this.state.storage.get(storeKey);
    if (!data) return { locked: false };
    const locked = !!data.lockedUntil && data.lockedUntil > Date.now();
    return { locked, lockedUntil: locked ? data.lockedUntil : void 0 };
  }
};

